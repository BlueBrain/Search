import abc


class Searcher(abc.ABC):

    @abc.abstractmethod
    def query(self,
              which_model,
              k,
              query_text,
              has_journal=False,
              date_range=None,
              deprioritize_strength='None',
              exclusion_text=None,
              deprioritize_text=None,
              verbose=True):

        """Do a search query."""
